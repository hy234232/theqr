// ── Language switcher ──
function applyLang(lang) {
  const dict = i18n[lang];
  if (!dict) return;

  document.documentElement.lang = lang;
  document.title = dict['page-title'] || document.title;

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const val = dict[el.dataset.i18n];
    if (val !== undefined) el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-ph]').forEach((el) => {
    const val = dict[el.dataset.i18nPh];
    if (val !== undefined) el.placeholder = val;
  });

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  localStorage.setItem('lang', lang);
}

document.querySelectorAll('.lang-btn').forEach((btn) => {
  btn.addEventListener('click', () => applyLang(btn.dataset.lang));
});

// ── Reveal on scroll ──
const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target);
      }
    });
  },
  { threshold: 0.18, rootMargin: '0px 0px -8% 0px' }
);

document.querySelectorAll('.reveal').forEach((el) => observer.observe(el));

// ── Form submission ──
const form = document.getElementById('lead-form');
if (form) {
  form.addEventListener('submit', async (e) => {
    e.preventDefault();
    const btn = form.querySelector('[type="submit"]');
    const status = document.getElementById('form-status');
    const lang = document.documentElement.lang || 'ko';

    btn.disabled = true;
    btn.textContent = lang === 'ko' ? '전송 중...' : 'Sending...';

    try {
      const res = await fetch('/api/inquiry', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(Object.fromEntries(new FormData(form))),
      });
      if (!res.ok) throw new Error('server');
      if (status) {
        status.textContent = lang === 'ko'
          ? '문의가 접수되었습니다. 감사합니다!'
          : 'Your inquiry has been received. Thank you!';
        status.className = 'contact-status contact-status-success';
      }
      form.reset();
    } catch {
      if (status) {
        status.textContent = lang === 'ko'
          ? '전송에 실패했습니다. 다시 시도해 주세요.'
          : 'Submission failed. Please try again.';
        status.className = 'contact-status contact-status-error';
      }
    } finally {
      btn.disabled = false;
      btn.textContent = i18n[lang]['contact-submit'] || '문의하기';
    }
  });
}

// ── Init: apply saved or default language ──
applyLang(localStorage.getItem('lang') || 'ko');
